export const translateKo = (value: string): string => {
    const notificationMatch = value.match(/^Notifications( \(.+\))$/)
    if (notificationMatch) {
        return `${koTranslations.Notifications}${notificationMatch[1]}`
    }

    const browseAppsMatch = value.match(/^Browse all apps \((\d+)\)$/)
    if (browseAppsMatch) {
        return `모든 앱 보기 (${browseAppsMatch[1]})`
    }

    return koTranslations[value] || value
}

