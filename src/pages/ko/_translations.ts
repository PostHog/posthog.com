export const translateKo = (value: string): string => {
    const normalizedValue = value
        .trim()
        .replace(/\u00a0/g, ' ')
        .replace(/[\u2010\u2011\u2012\u2013\u2014\u2212]/g, '-')

    const notificationMatch = value.match(/^Notifications( \(.+\))$/)
    if (notificationMatch) {
        return `${koTranslations.Notifications}${notificationMatch[1]}`
    }

    const browseAppsMatch = value.match(/^Browse all apps \((\d+)\)$/)
    if (browseAppsMatch) {
        return `모든 앱 보기 (${browseAppsMatch[1]})`
    }

    const companiesSignedUpMatch = value.match(/^(.+) companies signed up$/)
    if (companiesSignedUpMatch) {
        if (companiesSignedUpMatch[1].trim() === 'Tons of') {
            return '수많은 기업이 가입했습니다'
        }
        return `${companiesSignedUpMatch[1]}개 기업이 가입했습니다`
    }

    const unitPriceMatch = value.match(/^\$(.+)\/(.+)$/)
    if (unitPriceMatch) {
        const unitLabels: Record<string, string> = {
            event: '이벤트',
            recording: '녹화',
            request: '요청',
            row: '행',
        }
        return `${unitLabels[unitPriceMatch[2]] || unitPriceMatch[2]}당 $${unitPriceMatch[1]}`
    }

    return koTranslations[value] || koTranslations[normalizedValue] || koDeepMenuTranslations[normalizedValue] || value
}

